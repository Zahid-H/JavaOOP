package input.oop;

public class Calculator {
    // creating method
    int x;
    int y;
    int result;

    int doAddition(int a, int b) {

        return a + b;
    }

    int doSubtraction(int a, int b) {
        return a - b;
    }

    int doMultiplication(int a, int b) {
        return a * b;
    }

    int doDivision(int a, int b) {
        return a / b;
    }

    int doRemainder(int a, int b) {
        return a % b;
    }
}
